class Calculator:

    def add(self,n1,n2,):

        print(n1+n2)

    def add(self,n1,n2,n3):

        print(n1+n2+n3)

    def add(self,n1,n2,n3,n4):

        print(n1+n2+n3+n4)

cal_instance1=Calculator()

cal_instance1.add(10,20)

cal_instance1.add(10,20,30)

cal_instance1.add(10,20,30,40)

